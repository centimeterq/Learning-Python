# for loop syntax

for x in range(4) :
    print(x)

# loop list

listt = ["mikasa", "historia", "pieck", "annie"]

for x in listt :
    print(x)

# loop tuples

tuples = ("mikasa", "historia", "pieck", "annie")

for x in tuples :
    print(x)

# loop dict

dict = {
    "name" : "Historia Reiss",
    "couple" : "Eren Jeager",
    "son" : 1
}

for x in dict :
    print(dict[x])
