# lambda function adalah function yang di serdahanakan

# function normal

def my_function_normal (a , b) :
    return a + b

# di atas function normal dan di bawah adalah lambda function

my_function_lambda = lambda a, b : a + b

# di atas dan bawah adalah function yang sama tetapi yang bawah versi simplenya

# lambda hanya boleh 1 line jika ada line 2 sudah tidak valid
